package com.swipecard.model;

public class LineNO {
	private String WorkShopNo;
	private String LineNo;
	private int Use;
	private String LockPerson;
	private int LineSize;
	public String getWorkShopNo() {
		return WorkShopNo;
	}
	public void setWorkShopNo(String workShopNo) {
		WorkShopNo = workShopNo;
	}
	public String getLineNo() {
		return LineNo;
	}
	public void setLineNo(String lineNo) {
		LineNo = lineNo;
	}
	public int getUse() {
		return Use;
	}
	public void setUse(int use) {
		Use = use;
	}
	public String getLockPerson() {
		return LockPerson;
	}
	public void setLockPerson(String lockPerson) {
		LockPerson = lockPerson;
	}
	public int getLineSize() {
		return LineSize;
	}
	public void setLineSize(int lineSize) {
		LineSize = lineSize;
	}
}
